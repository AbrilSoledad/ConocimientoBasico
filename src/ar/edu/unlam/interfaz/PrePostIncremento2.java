package ar.edu.unlam.interfaz;

public class PrePostIncremento2 {

	public static void main(String[] args) {

		int preIncremento = 5;

		int postIncremento = 5;

		System.out.println("El numero para preincrementar y postincrementar es el 5");

		System.out.println("El numero con PREincremento es: " + ++preIncremento);

		System.out.println("El numero con POSTincremento es: " + postIncremento++);

	}

}
